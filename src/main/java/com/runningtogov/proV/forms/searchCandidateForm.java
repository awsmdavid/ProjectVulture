package com.runningtogov.proV.forms;

public class searchCandidateForm {
	private String cid;
	private String cand_name;
	private String error_msg;
	

	/**
	 * @return the cid
	 */
	public String getCid() {
		return cid;
	}

	/**
	 * @param cid the cid to set
	 */
	public void setCid(String cid) {
		this.cid = cid;
	}

	/**
	 * @return the cand_name
	 */
	public String getCand_name() {
		return cand_name;
	}

	/**
	 * @param cand_name the cand_name to set
	 */
	public void setCand_name(String cand_name) {
		this.cand_name = cand_name;
	}

	/**
	 * @return the error_msg
	 */
	public String getError_msg() {
		return error_msg;
	}

	/**
	 * @param error_msg the error_msg to set
	 */
	public void setError_msg(String error_msg) {
		this.error_msg = error_msg;
	}
	
}
